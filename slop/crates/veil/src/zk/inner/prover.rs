/// A zk-proof generated by the [`ZkProverContext`] at the end of the protocol.
///
/// Should be opened to a static [`ZkCnstrProof`] and mutable [`ZkVerificationContext`] before use by verifier.
#[derive_where(Clone; P: Clone)]
#[derive(Serialize, Deserialize)]
#[serde(bound(serialize = "P: Serialize", deserialize = "P: serde::de::DeserializeOwned"))]
pub struct ZkProof<GC: ZkIopCtx, P = ()> {
    pub(in crate::zk::inner) proof: ZkCnstrProof<GC, P>,
    pub(in crate::zk::inner) transcript: ProofTranscript<GC::EF>,
    /// PCS commitment transcript: metadata for each committed MLE.
    pub(in crate::zk::inner) pcs_commitment_transcript: Vec<PcsCommitmentEntry<GC::Digest>>,
}

/// The static proof data from [`ZkProof`] needed to verify linear and multiplicative constraints.
/// The multiplicative part is optional. PCS proofs are included when PCS was used.
#[derive_where(Clone; P: Clone)]
#[derive(Serialize, Deserialize)]
#[serde(bound(serialize = "P: Serialize", deserialize = "P: serde::de::DeserializeOwned"))]
pub struct ZkCnstrProof<GC: ZkIopCtx, P = ()> {
    pub mask_commitment: GC::Digest,
    pub zk_dot_product_proof: ZkDotTotalProof<GC, RsFromCoefficients<GC::EF>>,
    pub mul_proof_wrapper: Option<ZkMulCnstrProof<GC>>,
    /// PCS proofs for each evaluation claim (empty if no PCS was used).
    pub pcs_proofs: Vec<P>,
}

/// Just the data for proving multiplicative constraints.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(bound(serialize = "", deserialize = ""))]
#[allow(clippy::type_complexity)]
pub struct ZkMulCnstrProof<GC: ZkIopCtx> {
    /// Single shared commitment for all three vectors (a, b, c)
    pub commitment: GC::Digest,
    pub mul_proof: ZkHadamardAndDotsTotalProof<GC, RsInterpolation<GC::EF>>,
}

/// Error returned by [`ZkProverContext::prove`].
///
/// The two generic parameters are the [`ZkPcsProver::ProveError`] of the PCS
/// prover in use (e.g. the stacked-basefold error or `Infallible` for the no-PCS
/// path) and the [`TensorCsProver::ProverError`] of the merkleizer in use.
#[derive(Debug, Error)]
pub enum ZkProveError<PcsErr: Error + 'static, MerkErr: Error + 'static> {
    /// MLE-eval claims were registered but `prove`/`prove_without_pcs` was called
    /// without a PCS prover.
    #[error("PCS evaluation claims exist but no PCS prover was provided")]
    NoPcsProver,
    /// Two or more eval claims targeted the same committed oracle. This breaks
    /// zero-knowledge (but not soundness) — opening one commitment at multiple
    /// points reveals more information than the masking budget covers.
    #[error(
        "multiple eval claims on the same PCS commitment (index {commitment_index}); \
         this breaks zero-knowledge but not soundness"
    )]
    DuplicateEvalClaim { commitment_index: usize },
    /// The PCS prover's `prove_multi_eval` failed.
    #[error(transparent)]
    Pcs(PcsErr),
    /// The merkleizer failed during a commit or opening — surfaces from the
    /// dot-product / Hadamard subroutines used to discharge linear and
    /// multiplicative constraints.
    #[error(transparent)]
    Merkleizer(MerkErr),
    /// A constraint subroutine was handed inputs of an inconsistent shape (e.g. the
    /// Hadamard commit's three vectors differed in length). Surfaces from the
    /// multiplicative-constraint path.
    #[error("invalid constraint input shape: {0}")]
    InvalidShape(String),
}

impl<MerkErr: Error + 'static> ZkProveError<Infallible, MerkErr> {
    /// Widens an error from the no-PCS constraint-proving path (which can never produce a
    /// [`ZkProveError::Pcs`]) into the full error type for any PCS error parameter. Total and
    /// panic-free: the `Pcs` arm is discharged via the uninhabited [`Infallible`].
    ///
    /// An inherent method rather than a `From` impl: the latter would collide with the standard
    /// library's reflexive `From<T> for T` whenever the target `PcsErr` is itself `Infallible`.
    pub fn widen<PcsErr: Error + 'static>(self) -> ZkProveError<PcsErr, MerkErr> {
        match self {
            ZkProveError::NoPcsProver => ZkProveError::NoPcsProver,
            ZkProveError::DuplicateEvalClaim { commitment_index } => {
                ZkProveError::DuplicateEvalClaim { commitment_index }
            }
            ZkProveError::Pcs(never) => match never {},
            ZkProveError::Merkleizer(e) => ZkProveError::Merkleizer(e),
            ZkProveError::InvalidShape(s) => ZkProveError::InvalidShape(s),
        }
    }
}

/// Handle to a [`ZkProverContextInner`] that provides shared mutable access.
/// This is the main type users interact with for building zero-knowledge proofs.
///
/// The handle wraps the inner context in `Arc<Mutex<>>` to allow elements
/// returned from `add_values` to hold references back to the context, while
/// remaining `Send + Sync`.
///
/// # Type Parameters
/// * `GC` - The ZK IOP context type
/// * `MK` - The merkleizer type used for tensor commitments
/// * `PD` - The PCS prover data type (defaults to `()` when no PCS is used)
///
/// Contains a challenger which can be accessed using self.challenger()
///
/// Cloning is always available (and cheap): the only field is an `Arc`, so a clone
/// is a refcount bump and does not require `PD: Clone`.
#[derive_where(Clone)]
pub struct ZkProverContext<GC: ZkIopCtx, MK: ZkMerkleizer<GC>, PD = (), P = ()> {
    inner: Arc<Mutex<ZkProverContextInner<GC, MK, PD, P>>>,
}

/// Tracks linear constraints during zero-knowledge proof generation.
/// Also tracks masking values and their dot-product commitment.
/// Consumed at the end to produce a [`ZkProof`].
#[allow(clippy::type_complexity)]
#[derive_where(Clone; <MK as TensorCsProver<GC, CpuBackend>>::ProverData: Clone, PD: Clone, P: Clone)]
pub struct ZkProverContextInner<GC: ZkIopCtx, MK: ZkMerkleizer<GC>, PD = (), P = ()> {
    /// The challenger for Fiat-Shamir (access via ZkProverContext::challenger())
    challenger: GC::Challenger,

    // transcript is represented as a jagged array, the breakpoints are the index starts of each piece
    transcript: ProofTranscript<GC::EF>,

    // Need to keep track of where in masks we are to generate the masked values
    masks: Vec<GC::EF>,
    masks_current_index: usize,

    // Linear combinations of values that need to equal 0
    // Each constraint is a vector of (coefficient, index in masked_values as a jagged array)
    lin_constraints: Vec<TranscriptLinConstraint<GC::EF>>,

    // Multiplicative constraints (if needed)
    // Each constraint is a vector of indices a,b,c where a*b=c
    mul_constraints: Option<Vec<TranscriptMulConstraint<GC::EF>>>,

    // Commitments generated on initialization
    mask_commitment: GC::Digest,
    prover_secret_data: ZkVectorProverData<
        GC,
        <MK as TensorCsProver<GC, CpuBackend>>::ProverData,
        RsFromCoefficients<GC::EF>,
    >,

    expressions: Vec<ZkExpression<GC::EF, ProverElement<GC::EF>>>,

    // PCS commitment entries with their prover data. The data is wrapped in `Option`
    // so it can be moved out (via `take_prover_data`) when the commitment is opened,
    // rather than cloned. Each commitment is opened at most once (duplicate eval
    // claims are rejected) and the data is discarded after proving, so taking is safe.
    pcs_commitments: Vec<(PcsCommitmentEntry<GC::Digest>, Option<PD>)>,

    // PCS proofs produced eagerly at each MLE-eval opening (in opening order). Moved into the
    // final `ZkCnstrProof` by `prove`.
    pcs_proofs: Vec<P>,

    // Commitments already opened, to reject a second opening of the same commitment (this would
    // break zero-knowledge). Checked at each eager opening.
    opened_commitments: std::collections::HashSet<MleCommitmentIndex>,

    // Debug: constraint debugger (only present when sp1_debug_constraints is enabled)
    #[cfg(sp1_debug_constraints)]
    debugger: crate::zk::inner::debug::ConstraintDebugger,
}

