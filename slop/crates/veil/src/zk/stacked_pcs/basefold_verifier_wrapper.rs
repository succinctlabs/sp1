//! Zero-knowledge-aware wrapper for [`BasefoldVerifier`] with custom batch evaluation support.
//!
//! This module provides [`ZkStackedPcsVerifier`], a wrapper around `StackedPcsVerifier` that
//! verifies Basefold proofs for pre-batched, extension-field MLEs. The code is largely
//! copy-pasted from the standard Basefold verifier with two key modifications:
//!
//! 1. **Extension field openings**: The commitment is over a 4-column base-field tensor
//!    (representing the base-field components of extension-field coefficients), so the
//!    FRI query phase works with extension-field evaluations reconstructed from base-field
//!    openings.
//!
//! 2. **Closure-based batch evaluation**: Instead of computing batch evaluations internally,
//!    `verify_trusted_ext_mle_evaluation` accepts a `compute_batch_evals` closure. This
//!    allows the caller (e.g., `verifier.rs`) to inject protocol-specific correction logic
//!    (such as RLC padding correction) without polluting this copy-pasted Basefold code.
//!
//! # Relationship to Other Modules
//!
//! - **Prover side**: Proofs verified here are generated by
//!   [`prove_from_batched_inputs`](crate::zk::stacked_pcs::basefold_prover_wrapper::prove_from_batched_inputs)
//!   in the prover wrapper.
//! - **Caller**: [`verifier.rs`](crate::zk::stacked_pcs::verifier) constructs the `compute_batch_evals` closure
//!   that captures the RLC padding vector and expected evaluations, then passes it to
//!   `verify_trusted_ext_mle_evaluation`.
//!
//! # Usage
//!
//! ```ignore
//! let verifier = ZkStackedPcsVerifier::new(stacked_verifier);
//!
//! // The caller constructs a closure that computes corrected batch evaluations
//! // from query indices and the tensor height.
//! let compute_batch_evals = |query_indices: &[usize], log_tensor_height: usize| -> Vec<GC::EF> {
//!     // ... correction logic using RLC padding, expected evals, etc.
//! };
//!
//! verifier.verify_trusted_ext_mle_evaluation(
//!     &commitment,
//!     eval_point,
//!     eval_claim,
//!     &proof,
//!     compute_batch_evals,
//!     &mut challenger,
//! )?;
//! ```

