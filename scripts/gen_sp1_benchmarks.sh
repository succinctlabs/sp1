#!/usr/bin/env bash
#
# Generate the entire Sp1Benchmarks/ folder for the apc-optimizer repo.
#
# Produces two benchmark sets in the same layout as apc-optimizer's
# OpenVmBenchmarks/:
#
#   Sp1Benchmarks/rsp/     - top-N costliest basic blocks of the rsp (reth) guest,
#                            eth mainnet block 21740137, ranked by cell PGO
#   Sp1Benchmarks/keccak/  - costliest software-keccak block(s) of keccak-bench
#
# Each set contains, per case:
#   apc_<rank>_pc<pc>.json.gz            - unoptimized APC (apc-optimizer input)
#   apc_<rank>_pc<pc>.powdr_opt.json.gz  - powdr's optimized APC (reference),
#                                          snapshotted before add_guards so it
#                                          carries no `is_valid` column
# plus manifest.json and apc_candidates.json.
#
# This is heavy: the rsp set executes a ~29M-gas block and builds every candidate
# APC. Run it on a big machine. Requires the SP1 toolchain (cargo prove) and the
# rsp input cache at examples/rsp/script/input/1/21740137.bin.
#
# Usage:
#   scripts/gen_sp1_benchmarks.sh [OUT_DIR]     (default: ./Sp1Benchmarks)
#
# Tunables (env):
#   RSP_TOP=100          top-ranked rsp candidates to export
#   KECCAK_TOP=1         top-ranked keccak candidates to export
#   KECCAK_HASHES=100    number of hashes the keccak-bench guest computes
#   SKIP_RSP=0           set to 1 to skip the rsp set
#   SKIP_KECCAK=0        set to 1 to skip the keccak set

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
OUT_DIR="$(mkdir -p "${1:-Sp1Benchmarks}" && cd "${1:-Sp1Benchmarks}" && pwd)"

RSP_TOP="${RSP_TOP:-100}"
KECCAK_TOP="${KECCAK_TOP:-1}"
KECCAK_HASHES="${KECCAK_HASHES:-100}"
SKIP_RSP="${SKIP_RSP:-0}"
SKIP_KECCAK="${SKIP_KECCAK:-0}"

echo "==> Sp1Benchmarks output: $OUT_DIR"

if [[ "$SKIP_RSP" != "1" ]]; then
  echo "==> Generating rsp set (top $RSP_TOP) ..."
  # Run from the rsp script dir so its ./input/<chain>/<block>.bin cache resolves.
  ( cd "$REPO_ROOT/examples/rsp/script" \
      && cargo run --release -p rsp-script --bin rsp-script -- \
           powdr-bench --out "$OUT_DIR" --top "$RSP_TOP" )
fi

if [[ "$SKIP_KECCAK" != "1" ]]; then
  echo "==> Generating keccak set (top $KECCAK_TOP, $KECCAK_HASHES hashes, software keccak) ..."
  # No --manual => manual=false => the pure-Rust keccak runs, producing large
  # hot basic blocks that APC into a big constraint machine.
  ( cd "$REPO_ROOT" \
      && cargo run --release -p keccak-bench-script -- \
           --num-hashes "$KECCAK_HASHES" --bench-out "$OUT_DIR" --bench-top "$KECCAK_TOP" )
fi

echo "==> Gzipping case files ..."
# Only the per-case apc_<rank>_pc<pc>{,.powdr_opt}.json files; manifest.json and
# apc_candidates.json are left uncompressed (matching OpenVmBenchmarks).
find "$OUT_DIR" -type f -name 'apc_*_pc*.json' -exec gzip -nf {} +

echo "==> Writing $OUT_DIR/README.md ..."
cat > "$OUT_DIR/README.md" <<'EOF'
# Sp1Benchmarks

apc-optimizer benchmark inputs generated from SP1 guests, mirroring the layout of
`OpenVmBenchmarks/`. Drop this folder into the apc-optimizer repo and drive it
with `OpenVmBenchmarks/benchmark.py` (point it at this directory).

## Sets

- `rsp/` — the top-ranked basic blocks of the rsp (reth) guest verifying eth
  mainnet block 21740137, ranked by cell PGO. The SP1 analog of `openvm-eth`.
- `keccak/` — the costliest software-keccak block(s) of the `keccak-bench` guest.

## Per-case files

- `apc_<rank>_pc<pc>.json.gz` — the unoptimized APC in powdr's `SymbolicMachine`
  format (flattened `{block, machine, subs, optimistic_constraints, bus_map}`).
  This is the apc-optimizer's input.
- `apc_<rank>_pc<pc>.powdr_opt.json.gz` — powdr's optimized machine
  (`{machine, bus_map}`), used as the reference the apc-optimizer is compared
  against. Snapshotted *after* `optimize()` but *before* `add_guards`, so it
  carries no `is_valid` guard column.
- `manifest.json` — ranking + per-case size stats.
- `apc_candidates.json` — powdr's full candidate dump (assembly view for the
  HTML report).

## Notes

- Field: SP1 uses **KoalaBear**; field elements are encoded accordingly (the
  apc-optimizer's parser is currently BabyBear — that's the consuming side to
  reconcile, not these inputs).
- Ranking is cell PGO's value ranking, which folds in powdr's effectiveness, so
  the top-N is close to but not exactly the N costliest blocks.

## Regeneration

Generated by `scripts/gen_sp1_benchmarks.sh` in the SP1 repo.
EOF

echo "==> Done. Sets under $OUT_DIR:"
for d in rsp keccak; do
  if [[ -d "$OUT_DIR/$d" ]]; then
    n=$(find "$OUT_DIR/$d" -name 'apc_*_pc*.json.gz' ! -name '*.powdr_opt.json.gz' | wc -l | tr -d ' ')
    echo "    $d: $n case(s)"
  fi
done
